package Java_Old._31_Operators;
public class _14_BitwiseComplement 
{
    public static void main(String[] args) 
    {
        int x=5;
        System.out.println(~x);

        int y=6;
        System.out.println(~y);

        int z=7;
        System.out.println(~z);



        int a=-10;
        System.out.println(~a);

        int b=-9;
        System.out.println(~b);
    }
}
